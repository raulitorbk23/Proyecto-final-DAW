    <h1>Política de Devoluciones y Reembolsos</h1>
    <p>En TEMPLE GYM, queremos que esté completamente satisfecho con su compra. Si no está completamente satisfecho, puede devolver su compra bajo las siguientes condiciones.</p>

    <h2>Devoluciones</h2>
    <p>Puede devolver los artículos dentro de los 30 días posteriores a la recepción de su pedido. Los artículos deben estar en su estado original, sin usar y en su empaque original.</p>

    <h2>Reembolsos</h2>
    <p>Una vez que recibamos su devolución, inspeccionaremos los artículos y le notificaremos sobre el estado de su reembolso. Si se aprueba, procesaremos el reembolso a su método de pago original en un plazo de 20 días hábiles.</p>

    <h2>Artículos No Retornables</h2>
    <p>Los siguientes artículos no son retornables:</p>
    <ul>
        <li>Ropa interior</li>
        <li>Tarjetas de regalo</li>
        <li>Productos en oferta</li>
    </ul>

    <h2>Contacto</h2>
    <p>Para iniciar una devolución, por favor contáctenos en contacto@templegym.com con su número de pedido y los detalles del producto que desea devolver.</p>
