    <h1>Política de Envíos</h1>
    <p>En TEMPLE GYM, nos comprometemos a entregar su pedido de manera rápida y segura. A continuación se detallan nuestros métodos de envío, tiempos y costos.</p>

    <h2>Métodos de Envío</h2>
    <p>Ofrecemos los siguientes métodos de envío:</p>
    <ul>
        <li>Envío estándar</li>
        <li>Envío exprés</li>
        <li>Envío internacional</li>
    </ul>

    <h2>Tiempos de Entrega</h2>
    <p>El tiempo de entrega depende de la ubicación y el método de envío seleccionado:</p>
    <ul>
        <li>Envío estándar: 5-7 días hábiles</li>
        <li>Envío exprés: 2-3 días hábiles</li>
        <li>Envío internacional: 10-15 días hábiles</li>
    </ul>

    <h2>Costos de Envío</h2>
    <p>Los costos de envío se calcularán al momento de realizar el pedido y dependerán del método de envío seleccionado y la ubicación de entrega.</p>

    <h2>Seguimiento de Envíos</h2>
    <p>Una vez que su pedido haya sido enviado, recibirá un correo electrónico con un número de seguimiento para que pueda monitorear el estado de su envío.</p>

    <h2>Contacto</h2>
    <p>Si tiene alguna pregunta sobre nuestra política de envíos, por favor contáctenos en contacto@templegym.com .</p>
